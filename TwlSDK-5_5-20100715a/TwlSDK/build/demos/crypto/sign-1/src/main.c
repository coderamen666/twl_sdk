/*---------------------------------------------------------------------------*
  Name:         GetStringLength

  Description:  Gets the length of a string.

  Arguments:    str: Pointer to a string

  Returns:      Length of string
 *---------------------------------------------------------------------------*/
static u32 GetStringLength(char* str)
{
    u32 i;
    for (i = 0; ; i++)
    {
        if (*(str++) == '\0')
        {
            return i;
        }
    }
}

/*---------------------------------------------------------------------------*
  Name:         PrintBinary

  Description:  Binary array output.

  Arguments:    ptr: Memory location to be output
                len: Output length

  Returns:      None.
 *---------------------------------------------------------------------------*/
static void PrintBinary(u8* ptr, int len)
{
#ifndef SDK_FINALROM
    int i;
    for (i = 0; i < len; i++)
    {
        if (i != 0)
        {
            OS_PutString(":");
        }
        OS_TPrintf("%02x", ptr[i]);
    }
#else
#pragma unused(ptr,len)
#endif
    return;
}

